package view.exceptions;

public class VueException extends Exception{

	private static final long serialVersionUID = 1L;

	public VueException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public VueException(String arg0) {
		super(arg0);
	}

	public VueException(Throwable arg0) {
		super(arg0);
	}

	

	
}
